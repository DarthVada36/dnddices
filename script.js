// Math.trunc - Recorta el n√πmero sin importar la distancia//

const randomm = Math.trunc(Math.random() * 6) + 1;
console.log(randomm);

// Math.ceil- siempre redondea hacia arriba//
const numero = Math.ceil(Math.random () * 6);
console.log(numero);

//Math.floor- redondea numeros hacia abajo//
const number = Math.floor(Math.random() * 6) + 1;
console.log(number);

//Math.round - redondea numeros al mas cercano (arriba o abajo)//
const numberr = Math.round(Math.random() * 5) + 1;
console.log(numberr);

